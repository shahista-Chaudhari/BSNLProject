package com.test;

public class MyFirstArrayProgram {

	public static void main(String[] args) {
		//Array Declare
		int[] age;
		
		//Array create
		age = new int[4];
		
		//Array initialize
		age[0]=34;
		age[1]=45;
		age[2]=35;
		age[3]=67;
		
		System.out.println(age);
		System.out.println(age[0]);
		System.out.println(age[1]);
		System.out.println(age[2]);
		System.out.println(age[3]);
}
}
